#include <stdio.h>

int main(void)
{
   int num1 = 10;
   int num2 = 5 ;

   printf("num1과 num2에 여러 가지 연산을 실행합니다. \n");
   printf("num1+num2는 %d입니다. \n", num1+num2);
   printf("num1-num2는 %d입니다. \n", num1-num2);
   printf("num1*num2는 %d입니다. \n", num1*num2);
   printf("num1/num2는 %d입니다. \n", num1/num2);
   printf("num1%%num2는 %d입니다. \n", num1%num2);

   return 0;
}
